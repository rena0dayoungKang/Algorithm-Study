
import java.util.*;

public class Main {
    public static void main(String[] args) {
        
          Scanner sc = new Scanner(System.in);
      
        while(sc.hasNextLine()){  // BufferedReader로 다음 입력값이 있는지 확인하기 위해서 !=null 로 확인하는 방법도 있음.
            String input = sc.nextLine();
            System.out.println(input);
        }
        
    }
    
}
